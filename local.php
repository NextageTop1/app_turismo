  <h2>O que levar</h2>
  <div class="line line-sm mt-1 mt-3"></div>
  <p id="p_description_ponto">
    Durante os passeios é aconselhável usar roupas leves, roupas de banho, tênis ou botas, meias grossas e boné ou chapéu;
    - O participante deve levar uma mochila com objetos pessoais, alimentos leves suficientes para todo o passeio, um
    cantil ou vasilha com água, uma lanterna, uma toalha, capa de chuva, um canivete pequeno, materiais básicos de
    primeiros socorros (medicamentos, esparadrapos, etc.) e uma muda de roupa extra;
    - A alimentação ideal para os passeios é: sanduíches naturais, frutas, chocolates, biscoitos, bebidas isotônicas,
    água e produtos energéticos;
    - Aconselhamos levar ainda, máquina fotográfica, protetor solar, repelente, cajado, óculos escuros e um agasalho
    para se proteger do frio. 
  </p>

